function table=results_table_func(results)
    feval_dist=[...
        mean(results(:,1)),...
        min(results(:,1)),...
        quantile(results(:,1),0.25),...
        median(results(:,1)),...
        quantile(results(:,1),0.75),...
        max(results(:,1))];

    table=[feval_dist,mean(results(:,2:end),1)];
return
